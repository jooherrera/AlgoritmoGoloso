package dto;

public class AristaDTO {
	
	private int _vertice1;
	private int _vertice2;
	
	public AristaDTO(int _vertice1, int _vertice2) {
		this._vertice1 = _vertice1;
		this._vertice2 = _vertice2;
	}

	public int get_vertice1() {
		return _vertice1;
	}
	public int get_vertice2() {
		return _vertice2;
	}

}
